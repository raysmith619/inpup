"""
Testing if - else in a while loop
"""
while True:
    astr = input("Enter astr:")
    bstr = input("Enter bstr:")
    if astr > bstr:
        print("astr:",astr,"is > bstr:",bstr)
    else:
        print("astr:",astr,"IS NOT > bstr:",bstr)
    

